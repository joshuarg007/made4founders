// Group templates by category for easier browsing
export const offeredTemplatesByCategory = offeredProductTemplates.reduce((acc, template) => {
  if (!acc[template.category]) {
    acc[template.category] = [];
  }
  acc[template.category].push(template);
  return acc;
}, {} as Record<string, OfferedProductTemplate[]>);

// Search templates
export const searchOfferedTemplates = (query: string): OfferedProductTemplate[] => {
  const lowerQuery = query.toLowerCase();
  return offeredProductTemplates.filter(t =>
    t.name.toLowerCase().includes(lowerQuery) ||
    t.description.toLowerCase().includes(lowerQuery) ||
    t.category.toLowerCase().includes(lowerQuery)
  );
};
