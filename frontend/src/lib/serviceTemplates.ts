// Group templates by category for easier browsing
export const templatesByCategory = serviceTemplates.reduce((acc, template) => {
  if (!acc[template.category]) {
    acc[template.category] = [];
  }
  acc[template.category].push(template);
  return acc;
}, {} as Record<string, ServiceTemplate[]>);

// Search templates
export const searchTemplates = (query: string): ServiceTemplate[] => {
  const lowerQuery = query.toLowerCase();
  return serviceTemplates.filter(t =>
    t.name.toLowerCase().includes(lowerQuery) ||
    t.vendor.toLowerCase().includes(lowerQuery) ||
    t.description.toLowerCase().includes(lowerQuery)
  );
};
